export const sum = (firstNum, secondNum) => {
  return firstNum + secondNum;
};

export const subtract = (firstNum, secondNum) => {
  return firstNum - secondNum;
};

export const multiply = (firstNum, secondNum) => {
  return firstNum * secondNum;
};

export const divide = (firstNum, secondNum) => {
  return firstNum / secondNum;
};
