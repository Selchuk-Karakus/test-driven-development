export const lessThan = (numFirst, numSecond) => {
  let isLessThan = numFirst > numSecond ? numSecond : numFirst;
  return isLessThan;
};

export const greaterThanOrEqual = (word) => {
  let wordLength = word.length;
  return wordLength;
};

export const toBeClose = (numFirst, numSecond) => {
  let total = numFirst + numSecond;
  return total;
};
